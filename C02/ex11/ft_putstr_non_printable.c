#include <unistd.h>

void		ft_putchar(char c)
{
	write(1, &c, 1);
}

int			is_printable(char c)
{
	if (!(c >= 0 && c <= 31) && c != 127)
		return (1);
	return (0);
}

void		ft_putstr_non_printable(char *str)
{
	int		i;
	char	*hex;

	i = 0;
	hex = "0123456789abcdef";
	while (str[i] != '\0')
	{
		if (is_printable(str[i]))
		{
			ft_putchar(str[i]);
		}
		else
		{
			ft_putchar('\\');
			ft_putchar(hex[str[i] / 16]);
			ft_putchar(hex[str[i] % 16]);
		}
		i++;
	}
}
